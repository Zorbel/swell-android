  /** Returns true if m1 and m2 are equal according to isEqual. */
  public static boolean areAllEqual(List<? extends DocumentDiffSnapshot> m1,
  List<? extends DocumentDiffSnapshot> m2) {
    if (m1.size() != m2.size()) return false;
    Iterator<? extends DocumentDiffSnapshot> i1 = m1.iterator();
    Iterator<? extends DocumentDiffSnapshot> i2 = m2.iterator();
    while (i1.hasNext()) {
      if (!isEqual(i1.next(), i2.next())) return false;
    }
    return true;
  }

